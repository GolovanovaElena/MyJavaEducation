package ru.golovanova.Inheritance;

public enum Colors {
    BLACK,
    RED,
    BLUE,
    GREEN,
    CYAN,
    MAGENTA,
    YELLOW,
    WHITE;

    public Colors getRed() {
        return Colors.RED;
    }

    public Colors getGreen() {
        return Colors.GREEN;
    }

    public Colors getBlue() {
        return Colors.BLUE;
    }

}
